/// Centralized spacing scale for consistent paddings/margins.
class AppSpacing {
  const AppSpacing._();

  static const double xxs = 2;  // hairline spacing
  static const double xs = 4;   // very tight spacing
  static const double sm = 8;   // compact
  static const double md = 12;  // default gap between cards/items
  static const double lg = 16;  // section padding
  static const double xl = 20;  // larger separation
  static const double section = 24; // block separation
  static const double gutter = 32;  // extra large spacing
}
